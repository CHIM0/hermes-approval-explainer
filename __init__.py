def _normalize_tool_calls(tool_calls: Any) -> list[dict[str, str]]:
    normalized = []
    if not tool_calls:
        return normalized
    for tool_call in tool_calls:
        function = _message_attr(tool_call, "function", None)
        name = _message_attr(function, "name", "") if function is not None else _message_attr(tool_call, "name", "")
        arguments = (
            _message_attr(function, "arguments", "")
            if function is not None
            else _message_attr(tool_call, "arguments", "")
        )
        normalized.append(
            {
                "id": str(_message_attr(tool_call, "id", "")),
                "name": str(name),
                "arguments_preview": _redact_and_truncate(
                    _stringify_text(arguments), MAX_ARG_CHARS
                ),
            }
        )
    return normalized


def _message_attr(value: Any, name: str, default: Any = None) -> Any:
    if isinstance(value, dict):
        return value.get(name, default)
    return getattr(value, name, default)


def _stringify_text(value: Any) -> str:
    if value is None:
        return ""
    if isinstance(value, str):
        return value
    return json.dumps(value, ensure_ascii=False, default=str)


def _remember(cache: OrderedDict[str, dict[str, Any]], key: str, value: dict[str, Any]) -> None:
    cache[key] = value
    cache.move_to_end(key)
    while len(cache) > MAX_CACHE_ITEMS:
        cache.popitem(last=False)


def _latest_value(cache: OrderedDict[str, dict[str, Any]]) -> dict[str, Any] | None:
    if not cache:
        return None
    return next(reversed(cache.values()))


