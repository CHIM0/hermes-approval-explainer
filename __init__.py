def _path_summary(raw_path: str) -> str:
    path = Path(os.path.expanduser(raw_path))
    if not path.is_absolute():
        path = Path.cwd() / path
    path = path.resolve()
    if not path.exists():
        return f"{path}: 路径不存在"

    if path.is_file():
        return f"{path}: 文件，{path.stat().st_size} bytes"

    if path.is_dir():
        file_count, dir_count = _count_tree(path, limit=5000)
        return f"{path}: 目录，采样 {file_count} 文件/{dir_count} 子目录"

    return f"{path}: 存在，但不是普通文件或目录"


def _extract_paths(command: str) -> list[str]:
    try:
        tokens = shlex.split(command)
    except ValueError:
        tokens = command.split()

    paths = []
    skip_next = False
    for token in tokens[1:]:
        if skip_next:
            skip_next = False
            continue
        if token in {"-C", "-o", "--output", "-f", "--file"}:
            skip_next = True
            continue
        if token.startswith("-"):
            continue
        if any(mark in token for mark in ["/", ".", "~"]) and not re.match(r"^https?://", token):
            paths.append(token.rstrip(";"))
    return list(dict.fromkeys(paths))


def _count_tree(path: Path, limit: int) -> tuple[int, int]:
    files = 0
    dirs = 0
    try:
        for child in path.rglob("*"):
            if child.is_dir():
                dirs += 1
            elif child.is_file():
                files += 1
            if files + dirs >= limit:
                break
    except Exception:
        pass
    return files, dirs


def _match_assistant_action_context(key: str, command: str) -> dict[str, Any] | None:
    direct = _assistant_action_cache.get(key)
    if direct and _action_context_mentions_command(direct, command):
        return direct
    for value in reversed(_assistant_action_cache.values()):
        if _action_context_mentions_command(value, command):
            return value
    return direct or _latest_value(_assistant_action_cache)


def _action_context_mentions_command(context: dict[str, Any], command: str) -> bool:
    command_prefix = command[:80]
    if not command_prefix:
        return False
    return command_prefix in json.dumps(context, ensure_ascii=False, default=str)


def _match_tool_context(key: str, command: str) -> dict[str, Any] | None:
    direct = _tool_cache.get(key)
