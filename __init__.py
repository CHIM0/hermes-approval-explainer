                "只基于输入事实，解释：为什么 agent 要调用这个工具、为什么 Hermes 判定危险、"
                "用户批准前应检查什么。不要决定是否批准，不要说命令已经执行。"
                f"输出 {config['bullet_count']} 条短项目符号，每条不超过 "
