def register(ctx):
    ctx.register_hook("pre_llm_call", _capture_turn_context)
    ctx.register_hook("post_api_request", _capture_assistant_action_context)
    ctx.register_hook("pre_tool_call", _capture_tool_call)
    ctx.register_hook(
        "pre_approval_request",
        lambda **kwargs: _explain_approval_request(ctx, **kwargs),
    )
    ctx.register_hook("post_approval_response", _record_approval_response)
    ctx.register_command(
        name="approval-explainer-last",
        handler=_last_explanation_command,
        description="Show the most recent approval explanation generated by approval-explainer.",
    )


def _capture_turn_context(
    session_id: str = "",
    user_message: str = "",
    model: str = "",
    provider: str = "",
    platform: str = "",
    **kwargs,
):
    key = _cache_key(session_id=session_id, **kwargs)
    _remember(
        _turn_cache,
        key,
        {
            "user_message": _redact_and_truncate(user_message, MAX_MESSAGE_CHARS),
            "provider": _redact_and_truncate(provider, 80),
            "model": _redact_and_truncate(model, 120),
        },
    )
    return None


def _capture_assistant_action_context(
    session_id: str = "",
    task_id: str = "",
    assistant_message: Any = None,
    model: str = "",
    platform: str = "",
    finish_reason: str = "",
    **kwargs,
):
    """Cache the model text/reasoning that immediately preceded tool calls."""
    key = _cache_key(session_id=session_id, task_id=task_id, **kwargs)
    content = _message_attr(assistant_message, "content", "")
    reasoning = (
        _message_attr(assistant_message, "reasoning", "")
        or _message_attr(assistant_message, "reasoning_content", "")
        or _message_attr(assistant_message, "reasoning_details", "")
    )
    tool_calls = _normalize_tool_calls(_message_attr(assistant_message, "tool_calls", []))
    if not content and not reasoning and not tool_calls:
        return None

    _remember(
        _assistant_action_cache,
        key,
        {
            "assistant_content_before_tool": _redact_and_truncate(
                _stringify_text(content), MAX_MESSAGE_CHARS
            ),
            "assistant_reasoning_before_tool": _redact_and_truncate(
                _stringify_text(reasoning), MAX_MESSAGE_CHARS
            ),
            "assistant_tool_calls": tool_calls,
        },
    )
    return None


def _capture_tool_call(
    tool_name: str = "",
    args: dict | None = None,
    task_id: str = "",
    session_id: str = "",
    **kwargs,
):
    key = _cache_key(session_id=session_id, task_id=task_id, **kwargs)
    _remember(
        _tool_cache,
        key,
        {
            "tool_name": tool_name,
            "args": _safe_json(args or {}, max_chars=MAX_ARG_CHARS),
        },
    )
    return None


def _explain_approval_request(
    ctx,
    command: str,
    description: str,
    pattern_key: str = "",
    pattern_keys: list[str] | None = None,
    session_key: str = "",
    surface: str = "",
    **kwargs,
):
    started = time.monotonic()
    pattern_keys = list(pattern_keys or ([pattern_key] if pattern_key else []))
    key = _cache_key(session_id=session_key, **kwargs)
    turn_context = _turn_cache.get(key) or _latest_value(_turn_cache)
    assistant_action_context = _match_assistant_action_context(key, command)
    tool_context = _match_tool_context(key, command)
    config = _load_config()
    explanation_key = _explanation_key(session_key, command, pattern_keys)
    approval_id = _approval_identity(session_key, command, pattern_keys)
    if config["skip_repeated_after_accept"] and explanation_key in _accepted_explanation_keys:
        _write_log(
            "pre_approval_explanation_skipped",
            {
                "timestamp": datetime.now().isoformat(timespec="seconds"),
                "command": command,
                "description": description,
                "pattern_key": pattern_key,
                "pattern_keys": pattern_keys,
                "surface": surface,
                "session_key": session_key,
                "explanation_key": explanation_key,
                "reason": "previously accepted in this plugin session",
            },
        )
        return None

    _pending_explanation_keys[approval_id] = explanation_key
    facts = _compact_facts(
        command=command,
        description=description,
        pattern_keys=pattern_keys,
        surface=surface,
        session_key=session_key,
        turn_context=turn_context,
        assistant_action_context=assistant_action_context,
        tool_context=tool_context,
        include_session_metadata=bool(config["include_session_metadata"]),
    )

    explanation = _fallback_explanation(facts)
    llm_error = None
    text, llm_error, llm_meta = _complete_explanation_with_wall_timeout(
        ctx, facts, config, turn_context
    )
    if text:
        explanation = text

    payload = {
        "timestamp": datetime.now().isoformat(timespec="seconds"),
        "command": command,
        "description": description,
        "pattern_key": pattern_key,
        "pattern_keys": pattern_keys,
        "surface": surface,
        "session_key": session_key,
        "explanation": explanation,
        "llm_error": llm_error,
        "llm_request": llm_meta,
        "model_action_context": assistant_action_context,
        "elapsed_ms": int((time.monotonic() - started) * 1000),
    }
    notification = _send_desktop_notification(
        explanation=explanation,
        command=command,
        description=description,
        config=config,
    )
    payload["notification"] = notification
    _set_last_explanation(payload)
    _write_log("pre_approval_explanation", payload)
    notification_cfg = _desktop_notification_config(config)
    should_fallback_to_stderr = (
        notification.get("status") != "disabled"
        and not notification.get("ok")
        and notification_cfg.get("fallback_to_stderr")
    )
    if config["print_to_stderr"] or should_fallback_to_stderr:
        _display_explanation(explanation, surface)
    return None


def _complete_explanation_with_wall_timeout(
    ctx,
    facts: dict[str, Any],
    config: dict[str, Any],
    turn_context: dict[str, Any] | None,
) -> tuple[str | None, str | None, dict[str, Any]]:
    """Cap approval latency even when Hermes' auxiliary fallback chain is slow."""
    wall_timeout = _positive_float(config.get("llm_wall_timeout"), fallback=8.0)
    extra_body, llm_meta = _provider_extra_body(config, turn_context)
    messages = _explanation_messages(facts, config)
    result_queue: queue.Queue[tuple[str | None, str | None, dict[str, Any]]] = queue.Queue(maxsize=1)

    def worker() -> None:
        try:
            if extra_body:
                result = _complete_with_extra_body(ctx, messages, config, extra_body)
            else:
                result = ctx.llm.complete(
                    messages=messages,
                    max_tokens=int(config["max_tokens"]),
                    temperature=float(config["temperature"]),
                    timeout=int(config["timeout"]),
                    provider=config["provider"] or None,
                    model=config["model"] or None,
                    purpose="approval-explainer.pre_approval_request",
                )
            text = result.text.strip() if getattr(result, "text", None) else None
            result_queue.put_nowait((text, None, llm_meta))
        except Exception as exc:
            result_queue.put_nowait((None, f"{type(exc).__name__}: {exc}", llm_meta))

    thread = threading.Thread(
        target=worker,
        name="approval-explainer-llm",
        daemon=True,
    )
    thread.start()
    try:
        return result_queue.get(timeout=wall_timeout)
    except queue.Empty:
        return None, f"TimeoutError: explanation LLM exceeded {wall_timeout:.1f}s wall timeout", llm_meta


